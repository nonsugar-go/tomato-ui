func ParseCheckPoint(app *model.App) {
	// app.Filename: show package filename (ex: show_package-2026-05-06_00-16-27.tar.gz)
	outFile := "checkpoint_param.xlsx" // output Excel file name

	e := excel.NewExcel(outFile)
	defer e.Close()
	err := processTarGzip(app.Filename, func(tempDir string) error {
		a := &AppEnv{E: e, InDir: tempDir}

		a.parseCPObjects()
		a.parseAccessRules()
		a.parseNATRules()

		return nil
	})
	if err != nil {
		slog.Error("failed to parse show package", slog.String("file", app.Filename))
		return
	}
	slog.Info("Excel file saved", slog.String("file", outFile))
}

type AppEnv struct {
	E     *excel.Excel
	InDir string
	Name  map[string]string
}

func (a *AppEnv) GetNamesByUIDs(uids []string) []string {
	names := make([]string, 0, len(uids))
	for _, uid := range uids {
		if name, ok := a.Name[uid]; ok {
			names = append(names, name)
		} else {
			slog.Warn("Failed to convert uid to name", slog.String("uid", uid))
		}
	}
	sort.Slice(names, func(i, j int) bool {
		a := strings.ToLower(names[i])
		b := strings.ToLower(names[j])
		if a == b {
			return names[i] < names[j]
		}
		return a < b
	})
	return names
}

func (a *AppEnv) JoinNames(uids []string) string {
	return join(a.GetNamesByUIDs(uids))
}

func (a *AppEnv) parseAccessRules() {
	pattern := filepath.Join(a.InDir, "* Network-Management server.json")
	files, err := filepath.Glob(pattern)
	if err != nil {
		slog.Error("fail to find files",
			slog.String("pattern", pattern), slog.String("error", err.Error()))
		os.Exit(1)
	}

	for _, file := range files {
		ruleName := strings.TrimSuffix(filepath.Base(file), " Network-Management server.json")
		var accessRules []AccessRule
		jsonData, err := os.ReadFile(file)
		if err != nil {
			slog.Error("Faild to read a file", slog.String("filename", file), slog.String("error", err.Error()))
			continue
		}
		err = json.Unmarshal(jsonData, &accessRules)
		if err != nil {
			slog.Error("JSON error", slog.String("error", err.Error()))
			continue
		}

		a.E.NewSheet("Access Rules - " + ruleName)
		a.E.Println("No", "Name", "Enabled", "Negate", "Sources", "Negate", "Destinations", "Negate", "Services", "Action",
			"Track", "Per Connection", "Per Session", "Enable Firewall Session", "Alert", "Accounting",
			"InstallOn", "Comments", "CustomField1", "CustomField2", "CustomField3")
		for _, r := range accessRules {
			if r.Type == "access-section" {
				a.E.Println("", fmt.Sprintf("%s - From %d to %d", r.Name, r.From, r.To))
			} else {
				a.E.Println(
					itoa(r.RuleNumber), r.Name, b2s(r.Enabled),
					b2s(r.SourceNegate), a.JoinNames(r.Source),
					b2s(r.DestinationNegate), a.JoinNames(r.Destination),
					b2s(r.ServiceNegate), a.JoinNames(r.Service),
					a.Name[r.Action],
					a.Name[r.Track.Type], b2s(r.Track.PerConnection), b2s(r.Track.PerSession), b2s(r.Track.EnableFirewallSession),
					r.Track.Alert, b2s(r.Track.Accounting),
					a.JoinNames(r.InstallOn),
					r.Comments,
					r.CustomFields.Field1,
					r.CustomFields.Field2,
					r.CustomFields.Field3,
				)
			}
		}
		a.E.AddTable()
	}
}

